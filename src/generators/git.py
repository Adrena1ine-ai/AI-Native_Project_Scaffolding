def init_git_repo(project_dir: Path, project_name: str, initial_commit: bool = True) -> bool:
    """
    –ò–Ω–∏—Ü–∏–∞–ª–∏–∑–∏—Ä–æ–≤–∞—Ç—å Git —Ä–µ–ø–æ–∑–∏—Ç–æ—Ä–∏–π
    
    Args:
        project_dir: –ü—É—Ç—å –∫ –ø—Ä–æ–µ–∫—Ç—É
        project_name: –ù–∞–∑–≤–∞–Ω–∏–µ –ø—Ä–æ–µ–∫—Ç–∞
        initial_commit: –°–æ–∑–¥–∞—Ç—å –ø–µ—Ä–≤—ã–π –∫–æ–º–º–∏—Ç
        
    Returns:
        True –µ—Å–ª–∏ —É—Å–ø–µ—à–Ω–æ
    """
    print(f"\n{COLORS.colorize('üîó Git...', COLORS.CYAN)}")
    
    # –ì–µ–Ω–µ—Ä–∏—Ä—É–µ–º —Ñ–∞–π–ª—ã
    generate_gitignore(project_dir, project_name)
    generate_gitattributes(project_dir)
    
    try:
        # git init
        result = subprocess.run(
            ["git", "init"],
            cwd=project_dir,
            capture_output=True,
            text=True
        )
        
        if result.returncode != 0:
            print(f"  {COLORS.warning('git init failed')}")
            return False
        
        print(f"  {COLORS.success('git init')}")
        
        # –ù–∞—Å—Ç—Ä–∞–∏–≤–∞–µ–º main –∫–∞–∫ default branch
        subprocess.run(
            ["git", "branch", "-M", "main"],
            cwd=project_dir,
            capture_output=True
        )
        
        if initial_commit:
            # git add .
            subprocess.run(
                ["git", "add", "."],
                cwd=project_dir,
                capture_output=True
            )
            
            # git commit
            result = subprocess.run(
                ["git", "commit", "-m", f"üéâ Initial commit ‚Äî {project_name}\n\nGenerated by AI Toolkit v3.0"],
                cwd=project_dir,
                capture_output=True,
                text=True
            )
            
            if result.returncode == 0:
                print(f"  {COLORS.success('Initial commit created')}")
            else:
                # –í–æ–∑–º–æ–∂–Ω–æ git –Ω–µ –Ω–∞—Å—Ç—Ä–æ–µ–Ω (user.email, user.name)
                print(f"  {COLORS.warning('Commit skipped (configure git user first)')}")
        
        return True
        
    except FileNotFoundError:
        print(f"  {COLORS.warning('git not installed')}")
        return False
    except Exception as e:
        print(f"  {COLORS.error(f'Git error: {e}')}")
        return False
